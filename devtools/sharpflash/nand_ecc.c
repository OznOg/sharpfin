/**
 * nand_trans_result - create ECC
 * @reg2:	line parity reg 2
 * @reg3:	line parity reg 3
 * @ecc_code:	ecc 
 *
 * Creates ECC code from line parity
 */
static void nand_trans_result(U16 reg1, U16 reg2, U16 reg3, U8 *ecc_code) {
	U8 a, b, i, tmp1, tmp2, tmp3;
	/* Initialize variables */
	a = b = 0x80;
	tmp1 = tmp2 = 0;
	
	/* Calculate first ECC byte */
	for (i = 0; i < 4; i++) {
		if (reg3 & a)		/* LP15,13,11,9 --> ecc_code[0] */
			tmp1 |= b;
		b >>= 1;
		if (reg2 & a)		/* LP14,12,10,8 --> ecc_code[0] */
			tmp1 |= b;
		b >>= 1;
		a >>= 1;
	}
	tmp1 = ~tmp1 ;
	
	/* Calculate second ECC byte */
	b = 0x80;
	for (i = 0; i < 4; i++) {
		if (reg3 & a)		/* LP7,5,3,1 --> ecc_code[1] */
			tmp2 |= b;
		b >>= 1;
		if (reg2 & a)		/* LP6,4,2,0 --> ecc_code[1] */
			tmp2 |= b;
		b >>= 1;
		a >>= 1;
	}
	tmp2 = ~tmp2 ;

	/* Calculate third ECC byte */
	tmp3 = (~reg1 << 2) & 0xFC ;		/* CP543210  --> ecc_code[2] */
	if ( (reg3&0x100) == 0 ) tmp3|=2 ;	/* LP17 --> ecc_code[2] */
	if ( (reg2&0x100) == 0 ) tmp3|=1 ;	/* LP16 --> ecc_code[2] */

	/* Store the Row and Column Parity Bits */
	ecc_code[1] = tmp1; /* LSByte Line Parity */
	ecc_code[0] = tmp2; /* MSByte Line Parity */
	ecc_code[2] = tmp3; /* Column Parity */
}

/**
 * nand_calculate_ecc - Calc 3 byte ECC code for 512 byte block
 * @dat:	raw data
 * @ecc_code:	buffer for ECC
 */
int nand_calculate_ecc(const U8 *dat, U8 *ecc_code)
{
	U16 idx, reg1, reg2, reg3;
	int j;
	
	/* Initialize variables */
	reg1 = reg2 = reg3 = 0;
	ecc_code[0] = ecc_code[1] = ecc_code[2] = 0;
	
	/* Build up column parity */ 
	for(j = 0; j < 512; j++) {
		
		/* Get CP0 - CP5 from table */
		idx = (U16) nand_ecc_precalc_table[dat[j]];
		reg1 ^= (idx & 0x3f);
		
		/* All bit XOR = 1 ? */
		if (idx & 0x40) {
			reg3 ^= (U16) j;
			reg2 ^= ~((U16) j);
			reg3 &= 0x1FF ;
			reg2 &= 0x1FF ;
		}
	}

	/* Populate the ECC Codes from the parity bits in reg1-3 */
	nand_trans_result(reg1, reg2, reg3, ecc_code);

	return 0;
}
